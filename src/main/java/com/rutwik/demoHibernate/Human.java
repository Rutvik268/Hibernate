package com.rutwik.demoHibernate;

public class Human {
	private int hid;
	private String hname;
	private int hage;
	
	public int getHid() {
		return hid;
	}
	public void setHid(int hid) {
		this.hid = hid;
	}
	public String getHname() {
		return hname;
	}
	public void setHname(String hname) {
		this.hname = hname;
	}
	public int getHage() {
		return hage;
	}
	public void setHage(int hage) {
		this.hage = hage;
	}
	
	
}
